const mongoose = require('mongoose');

const grievanceSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  message: {
    type: String,
    required: true
  },
  isRead: {
    type: Boolean,
    default: false
  }
});

module.exports = mongoose.model('Feedback', grievanceSchema);